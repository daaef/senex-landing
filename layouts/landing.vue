<template>
  <div>
    <app-modal
      :v-modal="showSide"
      initial-class="side-nav-con"
      final-class-open="side-nav-show"
    >
      <transition name="fade">
        <SideNav v-if="showSide" @show="show" @reset-scroll="resetScroll" />
      </transition>
    </app-modal>
    <div>
      <Nav
        :theme="{ backgroundColor: '#162F55', color: '#FFFFFF' }"
        @toggle-side="toggleSide"
      />
      <Nuxt />
    </div>
  </div>
</template>

<script>
import AppModal from '~/components/custom/AppModal.vue'
export default {
  components: { AppModal },
  data() {
    return {
      showSide: false,
      reset: false
    }
  },
  methods: {
    finalClass() {
      if (this.showSide) {
        return 'show-modal'
      } else {
        return 'hide-modal'
      }
    },
    toggleSide() {
      this.showSide = !this.showSide
    },
    show(val) {
      this.showSide = val
    },
    resetScroll(val) {
      this.reset = val
    }
  }
}
</script>

